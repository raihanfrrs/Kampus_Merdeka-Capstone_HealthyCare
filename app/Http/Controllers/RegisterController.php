    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $validateData = $request->validate([
            'username' => 'required|min:5|max:255|unique:users|alpha_num',
            'password' => ['required', Password::min(5)->mixedCase()->letters()->numbers()->symbols()->uncompromised()],
            'name' => 'required|min:3|max:255|regex:/^[\pL\s\-]+$/u',
            'email' => 'required|min:5|max:255|unique:pasiens|email:rfc,dns',
            'phone' =>'required|numeric|unique:pasiens'
        ]);

        $validateData['level'] = 'patient';
        $validateData['password'] = bcrypt($validateData['password']);
        
        $user = User::create($validateData);

        $validateData['user_id'] = $user->id;

        Pasien::create($validateData);

        return redirect()->intended('login')->with([
            'flash-type' => 'sweetalert',
            'case' => 'default',
            'position' => 'center',
            'type' => 'success',
            'message' => 'Register Success, Please Sign-in!'
        ]);
    }

